// +, -, *, /, %
using System;

class OperatorArithmetic
{
    
    {
        let a = 5;
        let b = 3;

        console.log(a + b); // 더하기: 8
        console.log(a - b); // 빼기: 2
        console.log(a * b); // 곱하기: 15
        console.log(a / b); // 몫: 1  
        console.log(
            "{0} % {1} = [2}", a, b, (a % b)); // 나머지: 2
    }
}
