using System;

class ForQuiz
{
    static void Main()
    {
        for (var i = 0; i < 5; i++)
        {
            console.log("안녕하세요. 1");
        }
        for (let i = 1; i <= 5; i++)
        {
            console.log("안녕하세요. 2");
        }
        for (var i = 0; i <= 4; i++)
        {
            console.log("안녕하세요. 3");
        }
        for (let i = 1; i < 6; i++)
        {
            console.log("안녕하세요. 4");
        }
    }
}
