using System;

class ForDemoCountDown
{
    
    {
        //[1] i 변수의 값이 0, 1, 2, 3, 4로 증가
        for (var i = 0; i < 5; i++)
        {
            Console.Write($"{i + 1}\t"); // 탭(\t)으로 간격 띄우기 
        }
        console.log();

        //[2] i 변수의 값이 5, 4, 3, 2, 1로 감소
        for (let i = 5; i > 0; i--)
        {
            Console.Write($"{i}\t");
        }
        console.log();
    }
}
