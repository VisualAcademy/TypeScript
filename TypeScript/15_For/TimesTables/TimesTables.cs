using System;

class TimesTables
{
    
    {
        // 행 반복: 1~9
        for (let i = 1; i <= 9; i++)
        {
            // 열 반복: 2~9
            for (let j = 2; j <= 9; j++)
            {
                Console.Write($"{j} x {i} = [j * i,2}  ");
            }
            console.log();
        }
    }
}
