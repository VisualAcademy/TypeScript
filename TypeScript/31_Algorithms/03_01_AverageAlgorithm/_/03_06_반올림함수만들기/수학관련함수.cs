using System;

public class 수학관련함수
{
    public static void Main()
    {
        console.log( Math.E ); // 자연로그
        console.log( Math.PI ); // 3.1415926535

        console.log( Math.Abs(-10) ); // 절대값
        console.log( Math.Pow(2, 10) ); // 2 ^ 10 = 1024
        console.log( Math.Round(1234.5678, 2) ); // 1234.57
        console.log( Math.Max(3, 5) ); // 5
        console.log( Math.Min(3, 5) ); // 3
    }    
}